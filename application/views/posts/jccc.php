<h3>What is JoCo Cruise Crazy?</h3>

<p><a href="http://jococruisecrazy.com">JoCo Cruise Crazy</a> is a week-long Caribbean cruise featuring internet rock star Jonathan Coulton, comedy music duo Paul and Storm, and a bevy of their nerd-famous friends and acquaintances. The cruise is attended by a group of fans and creators colloquially known as Sea Monkeys, who self-organise so much programming on top of the official schedule that the running joke has it that JoCo himself doesn't even need to be there.</p>

<h3>Who am I?</h3>

<p>My name is Sara Chicazul, and I like to make things.</p>

<p>I attended the first JoCo Cruise Crazy in 2011, which inspired me to get more serious about following my artistic pursuits. I worked harder at my goal of making all the clothes I wear, as well as taking on commissions for clothing and costumes for other people. For JCCC 2 and 3, I funded my trip through selling fezzes and other custom items.</p>

<h4>Wait, fezzes?</h4>

<p>One of the annual events aboard the cruise is the Paul F. Thompkins Memorial Moustache Formal and Feztravaganza, at which both moustaches and fezzes are recommended (though optional) accessories. Plus, fezzes are cool now. I heard it from a Doctor.</p>

